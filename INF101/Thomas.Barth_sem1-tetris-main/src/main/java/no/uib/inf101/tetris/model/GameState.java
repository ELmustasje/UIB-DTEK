package no.uib.inf101.tetris.model;

/**
 * Represents the states of the game.
 */
public enum GameState {
    /**
     * Represents an active game state, where the game is currently being played.
     */
    ACTIVE_GAME,

    /**
     * Represents a game over state, where the game has ended.
     */
    GAME_OVER
}
